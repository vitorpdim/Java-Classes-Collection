public class OperadorAritmetico {
	
	public static void main (String[] args) {
		
		int a, b;
		double c;
		a = 3;
		b = 2;
		c = (double)(a/b);
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("c = a / c");
		System.out.println("c = " + c);
		
	}
}

