public class Ex06lista7 {
    public static void main(String[] args) {

        int tabuada, num, produto;
        tabuada = 8;

        for(num = 0; num <= 10; num++ ){
            produto = tabuada * num;
            System.out.println( tabuada + " x " + num + " = " + produto);
        }
    }
}

