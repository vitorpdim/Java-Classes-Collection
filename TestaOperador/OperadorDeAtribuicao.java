public class OperadorDeAtribuicao {
	
	public static void main (String[] args) {
		int x = 1;
		
		System.out.println("x = " + x);
		x = x + 3;
		System.out.println("x = x+3 => " + x);
		x += 3;
		System.out.println("x += 3 => " + x);
		x = x - 2;
		System.out.println("x = x - 2 => " + x);
		x -= 2;
		System.out.println("x -= => " + x);                                                                                                   
	}
}
